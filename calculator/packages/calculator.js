
const calculator = {
    
    sum: function (n1, n2) {
        return n1+n2
    },

    sub: function (n1, n2) {
        return n1-n2
    },

    mul: function (n1, n2) {
        return n1*n2
    },

    div: function (n1, n2) {
        return n1/n2
    },

    pot: function (n, ex) {
        return n**ex
    }

}

export default calculator